#Creating a List
My_List=['Dipak','Bista',28,2023,9]
#Print out inside List Elements
print(My_List)
#Displays length of My_List
print(len(My_List))
#Display index of My_List very first element
print(My_List[0])
#Displays all the elements of My_List
print(My_List[0:])
#Will print from zero element excludes Third Element
print(My_List[0:3])
#1 is default stepover
print(My_List[0:4:1])
#Stepover by 2
print(My_List[0:4:2])
#Reverse The List
print(My_List[::-1])

